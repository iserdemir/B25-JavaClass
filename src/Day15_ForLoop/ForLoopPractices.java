package Day15_ForLoop;

public class ForLoopPractices {
    public static void main(String[] args) {
        for (int i = 100; i > 49; i--){
            System.out.println("How you doing?");
        }
        for (int a = 1; a < 100; a++){
            if (a % 5 == 0){
                System.out.println("I'm doing okay!");
            }
        }
    }
}
