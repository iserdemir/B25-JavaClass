package Day07_Operators;

public class ShortHandOperators {
    public static void main(String[] args) {
        int number = 192;

        System.out.println("number = " + number);
        
        number = 324234;

        System.out.println("number = " + number);
        
        String word = "SMD";
        System.out.println("word = " + word);
        
        word = "DMS";

        System.out.println("word = " + word);

        System.out.println("------------------");

        word = "Hearth";
        word += " Break";

        System.out.println("word = " + word);
                
    }
}
