package Day04_Variables;

public class PrimitivesIntro {
    public static void main(String[] args) {
        char ch1 = 1905;
        char c2 = 2582;
        char c3 = 2503;
        char c4 = 1993;
        System.out.println("ch1 = " + ch1);
        System.out.println("c2 = " + c2);
        System.out.println("c3 = " + c3);
        System.out.println("c4 = " + c4);
    }
}
