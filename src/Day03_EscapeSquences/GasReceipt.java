package Day03_EscapeSquences;

public class GasReceipt {
    public static void main(String[] args) {
        System.out.println();
        System.out.println("\t\tMcClean Store");
        System.out.println();
        System.out.println("\t09.05am\t\t02.14.2020");
        System.out.println();
        System.out.println("Gallons:\t6.78");
        System.out.println("Price\\Gallon:\t$ 3.01");
        System.out.println("Fuel Total:\t$" + 3.01*6.78);
    }
}
